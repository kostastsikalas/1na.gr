/** Υπολογισμός μορίων για συγκεκριμένη σχολή */
export function calcPoints(grades: number[], coefficients: [number, number, number, number]): number {
  const sum = grades.reduce((acc, g, i) => acc + g * coefficients[i], 0);
  return Math.round(sum * 1000);
}

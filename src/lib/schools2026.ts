export function calcPoints(
  grades: number[],
  coefficients: [number, number, number, number],
  specialSubjectGrade?: number,
  specialSubjectPct?: number,
): number {
  // Main 4 subjects contribution
  const mainSum = grades.reduce((acc, g, i) => acc + g * coefficients[i], 0);
  let total = mainSum;
  
  // If school has a special subject, add its weighted contribution
  if (specialSubjectPct && specialSubjectGrade !== undefined) {
    total += specialSubjectGrade * (specialSubjectPct / 100);
  }
  
  return Math.round(total * 1000);
}
